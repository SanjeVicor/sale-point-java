    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        if (!this.nameTxt.getText().isEmpty() && !this.priceTxt.getText().isEmpty()) {
            Product product = new Product(lastId, this.nameTxt.getText(), Float.parseFloat(this.priceTxt.getText()));
            list.add(product);
            Vector row = new Vector();

            row.add(product.getId());
            row.add(product.getName());
            row.add(product.getPrice());
            DefaultTableModel model = (DefaultTableModel) this.productsTbl.getModel();
            model.addRow(row);

            lastId++;

            this.nameTxt.setText("");
            this.priceTxt.setText("");
        } else {
            showMessageDialog(null, "Valores Incompletos", "Valores Incompletos", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private boolean searchId(Vector e, int id) {

        if (e.get(0).equals(id)) {
            return true;
        }
        return false;
    }

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) this.productsTbl.getModel();
            Vector data = new Vector();
            data = model.getDataVector();

            System.out.println(data);

            int id = Integer.parseInt(this.searchTxt.getText());

            for (int i = 0; i < data.size(); i++) {
                if (searchId((Vector) data.get(i), id)) {
                    Vector data1 = (Vector) data.elementAt(i);
                    String name = (String) data1.get(1);
                    float price = (Float) data1.get(2);
                    this.search_nameTxt.setText(name);
                    this.search_priceTxt.setText(Float.toString(price));
                    break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al capturar el ID del producto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void sellBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellBtnActionPerformed
        try {
            if (!this.search_priceTxt.getText().isEmpty() && !this.searchTxt.getText().isEmpty()) {

                DefaultTableModel model = (DefaultTableModel) this.productsTbl.getModel();
                int id = Integer.parseInt(this.searchTxt.getText());

                Vector data = new Vector();
                data = model.getDataVector();

                for (int i = 0; i < data.size(); i++) {
                    if (searchId((Vector) data.get(i), id)) {
                        model.removeRow(i);
                        break;
                    }
                }

                totalSold += Float.parseFloat(this.search_priceTxt.getText());

                this.totalTxt.setText(Float.toString(totalSold));
                this.searchTxt.setText("");
                this.search_nameTxt.setText("");
                this.search_priceTxt.setText("");
            }else{
                JOptionPane.showMessageDialog(this, "Error al capturar el ID del producto", "Error", JOptionPane.ERROR_MESSAGE);
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al capturar el ID del producto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_sellBtnActionPerformed

